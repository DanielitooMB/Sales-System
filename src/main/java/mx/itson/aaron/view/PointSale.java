    private void refrescarTabla() {
        DefaultTableModel model = (DefaultTableModel) CartTbl.getModel();
        model.setRowCount(0);
        for (Object[] fila : psController.getDataParaTabla()) {
            model.addRow(fila);
        }
        txtlbl1.setText("$" + psController.calcularTotal());
    }

